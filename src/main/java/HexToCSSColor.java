	public static String getCSSColorFromHex(String hex) {
		Color c = Color.decode(hex);
		double bestScore = Double.POSITIVE_INFINITY;
		String bestString = null;
		for (Map.Entry<Color, String> entry : colorTable.entrySet()) {
			Color b = entry.getKey();
			double score = Math.pow(c.getRed() - b.getRed(), 2) + Math.pow(c.getGreen() - b.getGreen(), 2) + Math.pow(c.getBlue() - b.getBlue(), 2);
			if (score < bestScore) {
				bestScore = score;
				bestString = entry.getValue();
			}
		}
		return bestString;
	}
